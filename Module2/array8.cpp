/******************************************************************************
Two dim array 
*******************************************************************************/
#include <iostream>
using namespace std;
int main()
{
    int arr[3][3] = {
    //.0,0 0,1 0,2
        {1,2,3},
        {4,5,6},
        {7,8,9}
    };
    cout << "Basic 2D array: "<<endl;
    for (int i=0; i<3;i++){
        for (int j = 0; j<3;j++){
            cout << arr[i][j] << " ";
            // 0,0  0,1   0,2
            // 1,0  1,1   1,2 
            // 2,0  2,1   2,2
        }
        cout << endl;
    }
    return 0;
}
